import authService from '../services/auth.service.js';
import { successResponse } from '../utils/response.js';
import { z } from 'zod';

/**
 * @swagger
 * components:
 *   schemas:
 *     User:
 *       type: object
 *       properties:
 *         id:
 *           type: string
 *         uid:
 *           type: string
 *         email:
 *           type: string
 *         name:
 *           type: string
 *         role:
 *           type: integer
 *           description: "0: user, 1: shop, 2: admin"
 *     AuthResponse:
 *       type: object
 *       properties:
 *         success:
 *           type: boolean
 *         status:
 *           type: integer
 *         data:
 *           type: object
 *           properties:
 *             user:
 *               $ref: '#/components/schemas/User'
 *             firebaseToken:
 *               type: string
 *
 * @swagger
 * tags:
 *   name: Authentication
 *   description: User login and registration
 */
class AuthController {
  /**
   * @swagger
   * /api/v1/auth/login:
   *   post:
   *     summary: Login a user
   *     tags: [Authentication]
   *     requestBody:
   *       required: true
   *       content:
   *         application/json:
   *           schema:
   *             type: object
   *             properties:
   *               email:
   *                 type: string
   *               password:
   *                 type: string
   *               idToken:
   *                 type: string
   *     responses:
   *       200:
   *         description: Logged in successfully
   *         content:
   *           application/json:
   *             schema:
   *               $ref: '#/components/schemas/AuthResponse'
   *       401:
   *         description: Authentication failed
   *       404:
   *         description: User not found in MongoDB
   */
  async login(req, res, next) {
    try {
      const schema = z.object({
        email: z.string().email().optional(),
        password: z.string().optional(),
        idToken: z.string().optional(),
      }).refine(data => data.idToken || (data.email && data.password), {
        message: "Either idToken (Social) or Email & Password (Manual) must be provided",
      });

      const credentials = schema.parse(req.body);

      // 1. Authenticate via Service (Firebase)
      const authResult = await authService.login(credentials);

      // 2. Sync/Get from MongoDB
      let user;
      if (authResult.decodedToken) {
        // Social Login: Registration "goes within" the login flow
        user = await authService.syncUserWithMongo(authResult.decodedToken);
      } else {
        // Manual Login: User must already exist in MongoDB
        user = await authService.getUserByUid(authResult.uid);
      }

      if (!user) {
        const error = new Error('User authenticated in Firebase but not found in MongoDB. Please register first.');
        error.status = 404;
        throw error;
      }

      res.status(200).json(successResponse({
        user: {
          id: user._id,
          uid: user.firebaseUid,
          email: user.email,
          name: user.name,
          role: user.role,
        },
        // We return the idToken if generated by the backend
        firebaseToken: authResult.idToken || credentials.idToken
      }));
    } catch (error) {
      if (error instanceof z.ZodError) {
        error.status = 400;
      }
      next(error);
    }
  }
  /**
   * @swagger
   * /api/v1/auth/register:
   *   post:
   *     summary: Register a new user or shop
   *     tags: [Authentication]
   *     requestBody:
   *       required: true
   *       content:
   *         application/json:
   *           schema:
   *             type: object
   *             properties:
   *               email:
   *                 type: string
   *               password:
   *                 type: string
   *               idToken:
   *                 type: string
   *               name:
   *                 type: string
   *               username:
   *                 type: string
   *               role:
   *                 type: string
   *                 enum: [user, shop]
   *               shopName:
   *                 type: string
   *     responses:
   *       201:
   *         description: Registered successfully
   *         content:
   *           application/json:
   *             schema:
   *               $ref: '#/components/schemas/AuthResponse'
   *       400:
   *         description: User already exists or validation error
   */
  async register(req, res, next) {
    try {
      const schema = z.object({
        email: z.string().email().optional(),
        password: z.string().min(6).optional(),
        idToken: z.string().optional(),
        name: z.string().min(1, 'Name is required'),
        username: z.string().optional(),
        role: z.enum(['user', 'shop']).default('user'),
        shopName: z.string().optional(),
      }).refine(data => data.idToken || (data.email && data.password), {
        message: "Either idToken (Social) or Email & Password (Manual) must be provided",
        path: ["email"]
      });

      const data = schema.parse(req.body);

      // Register in Firebase (if manual) and MongoDB
      const user = await authService.register(data);

      res.status(201).json(successResponse({
        user: {
          id: user._id,
          uid: user.firebaseUid,
          email: user.email,
          name: user.name,
          role: user.role,
        }
      }, 201));
    } catch (error) {
      if (error instanceof z.ZodError) {
        error.status = 400;
      }
      next(error);
    }
  }



  /**
   * @swagger
   * /api/v1/auth/me:
   *   get:
   *     summary: Get current authenticated user
   *     tags: [Authentication]
   *     security:
   *       - bearerAuth: []
   *     responses:
   *       200:
   *         description: Current user details
   *         content:
   *           application/json:
   *             schema:
   *               type: object
   *               properties:
   *                 success:
   *                   type: boolean
   *                 status:
   *                   type: integer
   *                 data:
   *                   type: object
   *                   properties:
   *                     user:
   *                       $ref: '#/components/schemas/User'
   *       401:
   *         description: Unauthorized
   *       404:
   *         description: User not found
   */
  async getMe(req, res, next) {
    try {
      // req.user is set by authMiddleware
      const user = await authService.getUserByUid(req.user.uid);

      if (!user) {
        const error = new Error('User not found in database');
        error.status = 404;
        throw error;
      }

      res.status(200).json(successResponse({
        user: {
          id: user._id,
          uid: user.firebaseUid,
          email: user.email,
          name: user.name,
          role: user.role,
        }
      }));
    } catch (error) {
      next(error);
    }
  }
}

export default new AuthController();
